/**
 * Fisher-Yates shuffle algorithm
 * Returns a new shuffled array without modifying the original
 */
function shuffleArray<T>(array: T[]): T[] {
  const shuffled = [...array];
  for (let i = shuffled.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [shuffled[i], shuffled[j]] = [shuffled[j], shuffled[i]];
  }
  return shuffled;
}

/**
 * Select a random actor from the curated list
 * Optionally exclude specific actors (e.g., those that failed validation)
 */
export function selectRandomActor(excludeNames: string[] = []): string {
  const available = FEATURED_ACTORS.filter(name => !excludeNames.includes(name));

  if (available.length === 0) {
    throw new Error('No actors available after exclusions');
  }

  const shuffled = shuffleArray(available);
  return shuffled[0];
}

/**
 * Get multiple random actors (for retry scenarios)
 * Returns actors in random order, excluding any specified names
 */
export function getShuffledActors(excludeNames: string[] = []): string[] {
  const available = FEATURED_ACTORS.filter(name => !excludeNames.includes(name));
  return shuffleArray(available);
}
